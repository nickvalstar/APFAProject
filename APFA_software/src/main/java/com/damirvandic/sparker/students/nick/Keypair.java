package com.damirvandic.sparker.students.nick;


public class Keypair {

    public Key k1;
    public Key k2;
    public String datatype;
    public double pairscore;

    Keypair(Key k1, Key k2, String datatype, double pairscore){
        this.k1 = k1;
        this.k2 = k2;
        this.datatype = datatype;
        this.pairscore = pairscore;
    }
}
